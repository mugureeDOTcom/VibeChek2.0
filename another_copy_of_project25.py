import nltk
from nltk.sentiment import SentimentIntensityAnalyzer
from transformers import pipeline

# ðŸ“Œ Load the cleaned reviews
df = pd.read_csv("cleaned_reviews.csv")

# âœ… Initialize Sentiment Analyzers
nltk.download("vader_lexicon")
sia = SentimentIntensityAnalyzer()
sentiment_pipeline = pipeline("sentiment-analysis", model="nlptown/bert-base-multilingual-uncased-sentiment")

# ðŸŽ¯ Function to categorize sentiment based on star rating
def categorize_star_rating(rating):
    if rating >= 4.0:
        return "Positive"
    elif rating == 3.0:
        return "Neutral"
    else:
        return "Negative"

# ðŸŽ¯ Function to classify sentiment using VADER
def analyze_sentiment(text):
    score = sia.polarity_scores(text)["compound"]
    if score >= 0.05:
        return "Positive"
    elif score <= -0.05:
        return "Negative"
    else:
        return "Neutral"

# ðŸŽ¯ Function to classify sentiment using BERT (Swahili + English)
def analyze_multilingual_sentiment(text):
    result = sentiment_pipeline(text)[0]["label"]
    return "Positive" if "5" in result or "4" in result else "Negative" if "1" in result or "2" in result else "Neutral"

# ðŸ”¥ Apply Sentiment Analysis
df["Star_Category"] = df["rating"].apply(categorize_star_rating)
df["Sentiment_VADER"] = df["Cleaned_Review"].apply(analyze_sentiment)
df["Sentiment_BERT"] = df["Cleaned_Review"].apply(analyze_multilingual_sentiment)

# ðŸš€ Save Sentiment Results
df.to_csv("sentiment_results.csv", index=False)

